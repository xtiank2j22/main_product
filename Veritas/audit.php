                            <h4 class="text-navy text-bolder">Core Rolls:</h4>
                            <ul class="list">
                                <li>Ascertaining that risks are appropriately identified and managed.</li>
                                <li>Reviewing and appraising the soundness, adequacy, and application of accounting, financial, and other operating controls by promoting effective controls at a reasonable cost.</li>
                                <li>Evaluating the level of compliance with established University policies, plans and procedures, with federal and state laws and government regulations.</li>
                                <li>Ascertaining the stewardship of University assets.</li>
                                <li>Ascertaining the reliability and security of management data developed within the University.</li>
                                <li>Appraising the quality of performance in carrying out assigned responsibilities. (Does not include appraising the quality of teaching, research, nor patient care.)</li>
                                <li>Evaluating the effectiveness, efficiency and economy with which resources are deployed.</li>
                                <li>Recommending operating improvements.</li>
                                <li>Performing Construction Audits and Post Completion Reviews.</li>
                                <li>Responsibility and authority</li>
                            </ul>
                            <h4 class="text-navy text-bolder">The responsibilities of the Office of University Audit are:</h4>
                            <ul class="list">
                                <li>To inform and advise management, and to discharge this responsibility in a manner that is consistent with the Code of Ethics of the Association of College and University Auditors and the Institute of Internal Auditors, and in accordance with the mandatory elements of the International Professional Practices Framework (the Core Principles for the Professional Practice of Internal Auditing, the Code of Ethics, the Standards and the Definition of Internal Audit).</li>
                                <li>To coordinate internal audit and co-sourced activities with external audit and assurance resources so as to best achieve the objectives of the department.</li>
                                <li>To develop a flexible annual audit plan based on a prioritization determined by using relevant risk factors, including any risks or control concerns identified by management. Submit the plan to the Audit and Risk Assessment Committee of the board of Trustees, Medical Center Audit and Risk Assessment Committee and the respective Audit Committees of the affiliated entities for their review and approval.</li>
                                <li>To communicate to senior management and the respective Audit and Risk Assessment Committees any significant interim changes to the internal audit plan and the impact of resource limitations on the internal audit plan.</li>
                                <li>To review and adjust the internal audit plan, as necessary, in response to changes in University of Rochester’s business, risks, operations, programs, systems and controls.</li>
                                <li>To perform audit engagements, according to the internal audit plan, and communicate the results in a written report.</li>
                            </ul>
                            <h4 class="text-navy text-bolder">To meet with and report to the respective Audit and Risk Assessment Committees, at least on an annual basis, on whether:</h4>
                            <ul class="list">
                                <li>Appropriate action has been taken on significant audit findings.</li>
                                <li>Audit activities have been directed toward the highest exposures to risk and toward increasing effectiveness, efficiency, and economy of operations.</li>
                                <li>There is any unwarranted restriction on the staffing and authority of the Office of University Audit or on access by internal auditors to all University activities, records, property, and personnel.</li>
                                <li>The Office of University Audit resources are sufficient to achieve the submitted annual audit plan.</li>
                                <li>To follow up on engagement findings and corrective actions, and report periodically to senior management and the respective Audit and Risk Assessment Committees of any corrective actions not effectively implemented.</li>
                                <li>To investigate any allegations of unethical business practices and/or financial and operational misconduct to determine if allegations are substantiated and to prevent future occurrences and to work with the Office of Public Safety, when appropriate.</li>
                                <li>To provide reasonable assurance that trends and emerging issues that could impact the University are considered and communicated to senior management and the respective Audit and Risk Assessment Committees, as appropriate.</li>
                                <li>The Office of University Audit is provided with authority for full access to all of the University's records, properties, and personnel relevant to the subject under review. The Office of University Audit is free to review and appraise policies, plans, procedures, and records.</li>
                            </ul>
