                                              <p class="text-black text-bold text-justify">All groups require a pre-group screening in order to learn more about the group and whether or not it will meet the individual’s particular needs. A therapy group consists of a small number of people, usually not more than eight or ten, and one or two trained group Counsellor(s). Group therapy offers a unique opportunity of joining a small group of people who share common concerns. The purpose of the group is to provide a safe as well as challenging place in which to work on personal and interpersonal concerns. Members can discuss perceptions of each other and give and receive feedbacks on how they are perceived by others. Therapy groups are different from other groups because everything that happens in the group is confidential; what is talked about or disclosed is not discussed outside the group.</p>
