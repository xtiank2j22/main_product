        <div class="tab-pane fade" id="profile3">
         <h3 class="line-bottom mt-0 line-height-1">Hostel <span class="text-theme-color-2">Regulations</span></h3>
         <div class="drop-caps text-colored mb-60">
 <p> 

          Apart from the above, there are many issues involved in managing a collection of young, and in some cases
          undiscerning and exuberant people. Youngsters generally begin their lives with people whom they share
          consanguine ties.

          Consequently, living in the institution hostel may be for many the first time they are living outside the
          confines of their family homes and with those they share no consanguine ties.

          Ensuring that students live not only tolerantly and harmoniously, but equally with a convivial mindset is a
          great task and the first step towards achieving this is by having a code of conduct on how to conduct once
          affairs within the hostel, referred to as the Hostel Regulation. This is the main responsibility of the
          Student Affairs department and Student Affairs staff working in the hostels as Chief Hall Supervisors,
          Porters, etc.

          In the University of Nigeria, the highest ranking staff of the department in each hostel is called the Hall
          Supervisor. This person is in charge of the day to day running and routine management of the hostel. He or she
          is assisted by other staff who may also be Hall Supervisors, but are usually of lower ranks, then trailed by
          the Porters, Cleaners and Messengers.

          In addition to these staff, there is a hall government in each hall made of student officials, the head of
          which is called the Hall Governor, with his/her team who assist greatly in organizing students and in the
          regular enforcement of hall regulations.</p>
