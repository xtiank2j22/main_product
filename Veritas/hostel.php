           <p>

            One of the most important services delivered by the Student Affairs Department is arranging and providing
            not only acceptable but affordable accommodation. This is a duty of an outstanding importance especially to
            the first year students who, in most cases, are complete strangers in the environment.

            Although in respect to Federal Institutions, there is an open awareness of the Federal Government withdrawal
            of its financial support to hostel and hostel support services, UNN realizes that any institution that
            absolutely seizes to care about where students live, may produce intelligent/academically sound young people
            but certainly of questionable character, for when students live where they will, they also learn to behave
            how they will.

            Living in hostels is one of the most effective ways of helping students especially new ones to begin their
            journey towards the goal of imbibing the university culture and it is this culture that wholly transforms
            the student from what he or she is, to what he or she and the society expects them to become at the end of
            the learning period that spans their training in the University.

            The easiest way to get around this huge task especially in a thick populated institution is to make access
            to accommodation electronically and online. From experience, this reduces a lot of pressure and frustrations
            on the part of both the liaison staff of the department, and the students themselves.

            In addition to the advantage of reducing pressure on both staff and students, electronic registration
            ensures that no student registers without paying fees. It also helps the students as those who are tempted
            to use their fees for some other things and end up running into crisis later when the game is up are saved
            from day one that temptation. Electronic/online registration also ensure easy management of timelines for
            registration.</p>

