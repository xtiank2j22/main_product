        <div class="tab-pane fade" id="profile4">
         <h3 class="line-bottom mt-0 line-height-1">Hall <span class="text-theme-color-2">Warden</span></h3>
         <div class="drop-caps text-colored mb-60">
 <p>

          Given the level of care which the University believes the students need, there is also a staff of the
          institution, which is usually an academic or administrative staff, called a Hall Warden who is appointed by
          the head of the institution. This nomination is made by the recommendation of the Dean and is then put in
          charge of the hall. The Hall Warden has an office in the hostel where he/she is expected to come in at regular
          intervals to know the state of affairs within that particular hall.

          The duties of the Hall Warden are generally:

          Liaising with all service departments in the University to ensure that adequate services are rendered to
          students residing in the Hall.
          Developing of suitable programs that will give students in the Hall the opportunity for growth and development
          through sound scholarship, intra-mural sports and leadership activities.
          Development of adequate code of conduct for students living in the Hall, and enforcing it.
          Offering general and/or specific advice to the Hall Government in matters of welfare of students in the Hall.
          Educating students in the Hall on the rules, regulations and policy of the University.
          Performing other functions as may, from time and time, be delegated to him/her by the Vice-Chancellor.
          A Hall Warden is expected to hold meetings with students resident in the Hall at least twice a semester.</p>
