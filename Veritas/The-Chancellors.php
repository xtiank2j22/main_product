            <i class="mt-5 text-theme-color-2 text-bolder text-black font-20">The Chancellor</i>
            <p class="text-justify text-bold text-black">Kaigama was born in Kona, Taraba, on 31 July 1958. He studied for the priesthood at St. Augustine's Seminary in Jos. He was ordained a priest on 6 June 1981. At the Pontifical Gregorian University, he earned a doctorate in theology in 1991.</p>
            <p class="text-justify text-bold text-black">Pope John Paul II appointed him bishop of the newly established Roman Catholic Diocese of Jalingo on 3 February 1995. He was consecrated as bishop on 23 April 1995. The Bishop of Yola, Patrick Francis Sheehan OSA, was the principal consecrator, with co-consecrators Gregory Obinna Ochiagha, Bishop of Orlu, and Athanasius Atule Usuh, Bishop of Makurdi.</p>
            <p class="text-justify text-bold text-black">On 18 May 2000, Pope John Paul named Kaigama to succeed Gabriel Gonsum Ganaka as Archbishop of Jos. On 11 March 2019, Pope Francis named him as the Coadjutor Archbishop of Abuja and on 5 December 2019, he became the substantive Archbishop of Abuja Archdiocese to succeed John Cardinal Onaiyekan.</p>
            <p class="text-justify text-bold text-black">Kaigama was President of the Nigerian Bishops Conference from 2012 to 2018 and President of the Reunion of Episcopal Conferences of West Africa from 2016 to 2022. He has held positions as Chair of the Plateau State-convened "Interreligious Committee for Peace" from 2005-2007 and of the Christian Association of Nigeria in Plateau State from 2007 to 2010. Together with the late Emir of Wase, Alhaji Haruna Abdullahi, he has been involved in promoting mutual understanding between Christians and Muslims. He travels around the world to speak about peace and share his experience of dialogue/interfaith collaboration and peace building efforts.</p>
            <p class="text-justify text-bold text-black"><span id="dots"></span><span id="more" class="dismore">After the riots in Jos in January 2010, he calmed the situation and clarified the conflict in the international press. In the same year, he founded the Interfaith Youth Vocational Training Centre in Bokkos, Plateau State, which brings together Muslim and Christian youths as a proactive way of curbing the circle of violence on the Plateau.<br>
            In 2011, he founded the Dialogue, Reconciliation and Peace Centre, Jos, Plateau State. He also founded the Female Catechists Formation Centre in Kwall in 2007, Plateau State, dedicated to forming female agents of evangelization as Catechists.<br>
            On 25 July 2012, Pope Benedict XVI named Kaigama a member of the Pontifical Council for the Promotion of the New Evangelisation.<br>
            In 2014, as President of the Catholic Bishops Conference in Nigeria, he signed a letter that commended the Nigerian legislation that abhorred same-sex marriage. It noted the move as a courageous act and a bold step that upheld the dignity and sanctity of marriage even in the face of pressure from foreign governments. In his speech at the Catholic Bishops Conference in 2015, he reiterated the position of the Bishops to support legislation that contradicts “our cultural and religious norms of marriage”. “Our commitment to providing justice to those whose rights are unfairly violated is unwavering”, he said. He further stated that as with other pastoral cases, there would be pastoral response for such people. This position has been largely misunderstood and reported with absurdity as "the Nigerian Catholic Bishops asking the government to jail people with different sexual orientations.<br>
            Kaigama condemned foreign aid programs for a disproportionate emphasis on contraception. In 2014, he said: “In the first place, children die in infant mortality, in inter-tribal wars and diseases, but yet you come to say 'decrease your population and we will give you economic help.' We want food, we want education, we want good roads, healthcare. We are being given the wrong things and we are being asked to accept, simply because we are poor.<br>
            As President of the Catholic Bishops Conference of Nigeria, he was the Visitor to the Veritas University, Abuja, and as Archbishop of Abuja, he is its current Chancellor. He has been the Chancellor of Godfrey Okoye University, Enugu since 4 October 2013. By virtue of his position as President of the Reunion of the Episcopal Conferences of West Africa, he was the Grand Chancellor, L’université Catholique de l’Afrique de l’Ouest (UCAO) and the Chancellor, Catholic Institute of West Africa (CIWA).<br>
            In October 2015, the interreligious work which Archbishop Kaigama and Muslim leader Emir Muhammadu Mohammed Muazu of Kanam are doing in Nigeria was profiled in the Deutsche Welle article, “Nigeria: Religious dialogue in times of terror.<br>
            He was awarded two honorary doctorate degrees in Public Administration and Human Resource Management by Godfrey Okoye University, Enugu (2015) and Madonna University (2016) respectively.<br>
            He received an international award, the “Golden Doves Peace Award” from Italy in 2012 and the San Valentino Peace Award from the Diocese of Terni-Narni-Amelia in 2013, also in Italy. In 2014, he received the Religious Award of a Turkish Non-Governmental Organization, the UFUK Dialogue Foundation, among several other local awards.
